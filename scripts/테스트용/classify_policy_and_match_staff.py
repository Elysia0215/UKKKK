def classify_proposal(row):
    text = norm(" ".join([
        row.get("카테고리", ""),
        row.get("담당부서", ""),
        row.get("제안제목", ""),
        row.get("제안본문내용", ""),
    ])).lower()

    best = None
    best_score = -1
    best_hits = []

    for large, mid, small, flow, keywords in CLASSIFICATION_RULES:
        hits = []
        score = 0
        for keyword in keywords:
            if keyword.lower() in text:
                hits.append(keyword)
                score += 3 if len(keyword) >= 3 else 1

        category = row.get("카테고리", "")
        if category == "임신" and large == "임신·난임·생식건강":
            score += 2
        elif category == "출산" and large == "출산·산후 초기지원":
            score += 2
        elif category == "보육" and large == "보육·돌봄 인프라":
            score += 2
        elif category == "다자녀" and large == "다자녀·양육비·생활지원":
            score += 2
        elif category == "다문화" and "다문화" in mid:
            score += 3
        elif category == "위기임산부" and "위기임산부" in mid:
            score += 3

        if score > best_score:
            best = (large, mid, small, flow)
            best_score = score
            best_hits = hits

    if best_score <= 0:
        best = ("정보·상담·교육·거버넌스", "신청·서류·기준 개선", "행정절차·기준 개선", "전 생애주기 공통")

    trust = "상" if best_score >= 8 else "중" if best_score >= 4 else "하"
    return pd.Series({
        "대분류": best[0],
        "중분류": best[1],
        "세분류": best[2],
        "정책흐름": best[3],
        "분류근거_키워드": ", ".join(best_hits[:8]),
        "분류신뢰도": trust,
    })


