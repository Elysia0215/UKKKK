def load_staff(staff_path):
    raw = pd.read_excel(staff_path, header=None)
    # 1행 제목, 2행 헤더, 3행부터 데이터라는 원본 구조 기준
    staff = raw.iloc[2:, :5].copy()
    staff.columns = ["담당부서", "직원명", "전화번호", "담당업무", "부서위치"]
    staff = staff.dropna(subset=["담당부서", "전화번호"])
    for col in staff.columns:
        staff[col] = staff[col].map(norm)
    staff["업무키워드"] = staff["전화번호"].map(lambda x: STAFF_KEYWORDS_BY_PHONE.get(x, []))
    return staff.reset_index(drop=True)


def match_staff(row, staff_df):
    text = norm(" ".join([
        row.get("카테고리", ""),
        row.get("담당부서", ""),
        row.get("제안제목", ""),
        row.get("제안본문내용", ""),
        row.get("대분류", ""),
        row.get("중분류", ""),
        row.get("세분류", ""),
        row.get("분류근거_키워드", ""),
    ])).lower()

    scored = []
    for _, staff in staff_df.iterrows():
        score = 0
        matched = []
        phone = staff["전화번호"]

        for keyword in staff["업무키워드"]:
            if keyword.lower() in text:
                score += 10 if len(keyword) >= 4 else 5
                matched.append(keyword)

        # 담당업무 텍스트의 의미 있는 단어가 제안문에 직접 등장하면 약한 가점
        work_terms = set(re.findall(r"[가-힣A-Za-z0-9·]+", staff["담당업무"]))
        for term in work_terms:
            if len(term) >= 3 and term.lower() in text:
                score += 2
                if len(matched) < 8:
                    matched.append(term)

        if phone in LARGE_TO_PHONE_BOOST.get(row.get("대분류", ""), []):
            score += 3
        if phone in MID_TO_PHONE_BOOST.get(row.get("중분류", ""), []):
            score += 6

        if row.get("담당부서") and row.get("담당부서") in staff["담당부서"]:
            score += 7
            matched.append(row.get("담당부서"))

        scored.append({
            "score": score,
            "staff": staff,
            "matched": list(dict.fromkeys([m for m in matched if m])),
        })

    scored = sorted(scored, key=lambda x: x["score"], reverse=True)[:3]
    return scored


def main():
    proposal_path = Path(INPUT_PATHS["proposal_csv"])
    staff_path = Path(INPUT_PATHS["staff_xlsx"])
    output_path = Path(INPUT_PATHS["output_xlsx"])

    df = pd.read_csv(proposal_path, encoding="utf-8-sig")
    staff_df = load_staff(staff_path)

    classification = df.apply(classify_proposal, axis=1)
    result = pd.concat([df, classification], axis=1)

    match_cols = []
    first_counter = Counter()

    for _, row in result.iterrows():
        top3 = match_staff(row, staff_df)
        first_counter[top3[0]["staff"]["전화번호"]] += 1
        record = {"담당매칭방식": "규칙기반 키워드 점수 + 대/중분류 부스트"}
        for rank, item in enumerate(top3, start=1):
            staff = item["staff"]
            matched_text = ", ".join(item["matched"]) if item["matched"] else f"{row['대분류']} / {row['중분류']} 기반 후보"
            record[f"{rank}순위_담당부서"] = staff["담당부서"]
            record[f"{rank}순위_전화번호"] = staff["전화번호"]
            record[f"{rank}순위_매칭근거"] = f"점수 {item['score']}: {matched_text}"
            if rank == 1:
                record[f"{rank}순위_담당업무요약"] = staff["담당업무"][:160]
        match_cols.append(record)

    result = pd.concat([result, pd.DataFrame(match_cols)], axis=1)

    summary = (
        result.groupby(["대분류", "중분류", "세분류"])
        .size()
        .reset_index(name="건수")
        .sort_values("건수", ascending=False)
    )

    staff_summary = []
    for phone, count in first_counter.most_common():
        staff = staff_df.loc[staff_df["전화번호"] == phone].iloc[0]
        staff_summary.append({
            "전화번호": phone,
            "담당부서": staff["담당부서"],
            "직원명": staff["직원명"],
            "1순위_매칭건수": count,
            "담당업무요약": staff["담당업무"][:200],
        })
    staff_summary = pd.DataFrame(staff_summary)

    method = pd.DataFrame([
        ["분류 방식", "규칙 기반 키워드 매칭(rule-based keyword scoring)"],
        ["사용 모델", "KMeans/LLM/딥러닝 모델 사용 안 함"],
        ["KMeans 미사용 이유", "KMeans는 비지도 군집이라 담당부서 라우팅 근거 설명이 약함"],
        ["담당 매칭", "제안 텍스트 + 대/중/세분류 + 업무담당 담당업무 텍스트를 점수화하여 1·2·3순위 후보 산출"],
    ], columns=["항목", "내용"])

    with pd.ExcelWriter(output_path, engine="openpyxl") as writer:
        result.to_excel(writer, sheet_name="분류완료_데이터", index=False)
        summary.to_excel(writer, sheet_name="분류요약", index=False)
        staff_df.to_excel(writer, sheet_name="업무담당_원본", index=False)
        staff_summary.to_excel(writer, sheet_name="담당매칭_요약", index=False)
        method.to_excel(writer, sheet_name="분류방법_데이터구조", index=False)

    print(f"완료: {output_path}")


if __name__ == "__main__":
    main()
